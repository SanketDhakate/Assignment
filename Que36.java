/* Use an if-else statement to check if a number is even 
   or odd. */
class Que36 {
   public static void main(String[] args) {
      int num = 10;
      if (num % 2 == 0) {
         System.out.println(num + " is even.");
      } else {
         System.out.println(num + " is odd.");
      }
   }
}